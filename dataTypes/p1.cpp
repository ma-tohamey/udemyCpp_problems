#include<iostream>

using namespace std;

// same as udemy soultion
int main()
{
  double n1, n2; // it is better to make it double for deviding
  cin >> n1 >> n2;

  cout << n1 << " + " << n2 << " = " << n1 + n2 << "\n";
  cout << n1 << " - " << n2 << " = " << n1 - n2 << "\n";
  cout << n1 << " / " << n2 << " = " << n1 / n2 << "\n";
  cout << n1 << " * " << n2 << " = " << n1 * n2 << "\n";

  return 0;
}
